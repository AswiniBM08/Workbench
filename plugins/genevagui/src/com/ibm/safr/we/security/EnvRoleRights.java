import java.util.HashSet;
import java.util.Set;

import com.ibm.safr.we.constants.ComponentType;
import com.ibm.safr.we.constants.EditRights;
import com.ibm.safr.we.constants.Permissions;

public abstract class EnvRoleRights {
    
    protected Set<Permissions> permissions = new HashSet<Permissions>();
    
    public abstract EditRights getComponentRight(ComponentType type);
    
    public boolean hasPermission(Permissions permission) {
        return getPermissions().contains(permission);
    }

    public Set<Permissions> getPermissions() {
        return permissions;
    }
}
